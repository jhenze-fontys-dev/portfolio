// Purpose    : Resets development SQL databases for all configured dialects.
// Writes     : backend/data/sql/data.db (sqlite), configured Postgres database.
// Reads      : .env (DB_DIALECT_LIST, POSTGRES_*), dev/db/*/*.sql.
// Notes      : Uses external CLIs (sqlite3, psql). Destructive – dev use only.
